#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	while (n && *s1 && (*s1 == *s2))
	{
		s1++;
		s2++;
		n--;
	}
	return (n) ? (*(const unsigned char *)s1 - *(const unsigned char *)s2) : 0;
}
